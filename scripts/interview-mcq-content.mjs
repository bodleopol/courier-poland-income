export function buildInterviewMcq() {
  const langs = ['uk', 'en', 'es', 'ru'];
  const out = {};
  for (const lang of langs) {
    const specialties = buildSpecialties(lang);
    const catalog = CATALOG[lang];
    if (!catalog || catalog.length < 20) throw new Error(`MCQ catalog too small: ${lang}`);
    if (specialties.length < 40) throw new Error(`Specialties too few: ${lang}`);
    for (const row of catalog) {
      if (!row.stem.includes('{s}')) throw new Error(`stem missing {s}: ${lang}`);
      if (!Array.isArray(row.wrongs) || row.wrongs.length < 8) throw new Error('wrongs');
    }
    out[lang] = { specialties, catalog };
  }
  return out;
}
