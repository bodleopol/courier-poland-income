/**
 * Curated MCQ bank + combinatorial specialty grid for interview drill.
 * Consumed by scripts/emit-interview-tables.mjs → window.INTERVIEW_MCQ
 */

function buildSpecialties(lang) {
  const L = LEVELS[lang];
  const T = TRACKS[lang];
  const out = [];
  for (let li = 0; li < L.length; li += 1) {
    for (let ti = 0; ti < T.length; ti += 1) {
      const id = `lv${li}-tr${ti}`;
      out.push({ id, label: `${L[li]} — ${T[ti]}` });
    }
  }
  return out;
}

