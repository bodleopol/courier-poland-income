function pairFromId(firstArr, lastArr, id) {
  const nf = firstArr.length;
  const nl = lastArr.length;
  const cap = nf * nl;
  const i = ((id % cap) + cap) % cap;
  return `${firstArr[i % nf]} ${lastArr[Math.floor(i / nf) % nl]}`;
}

/** id is zero-based row index (n - 1). */
export function pickPersonFullName(langKey, id) {
  const L = BY_LOCALE[langKey];
  if (!L) return pairFromId(EN_FIRST, EN_LAST, id);
  return pairFromId(L.first, L.last, id);
}

function mix(seed, mod) {
  return (Math.imul(seed ^ 0x9e3779b9, 0x85ebca6b) >>> 0) % mod;
}

/** id is zero-based row index (n - 1). */
export function pickStartupDisplayName(langKey, id) {
  const L = BY_LOCALE[langKey] || BY_LOCALE.en;
  const a = L.coA[mix(id + 11, L.coA.length)];
  const b = L.coB[mix(id * 3 + 5, L.coB.length)];
  return L.startupFmt(a, b);
}
